// function arrow
const subtracao = (a, b) => a - b

console.log(subtracao(4, 5))

// function expression
const divisao = function (a, b) {
    return a / b
}

console.log(divisao(4, 2))

// named function expression
const multiplicacao = function multiplicacao (a, b) {
    return a * b
}

console.log(multiplicacao(4, 5))